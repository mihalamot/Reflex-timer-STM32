  MX_TIM10_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  ssd1306_Init();

  ssd1306_Fill(Black);
  char* msg = "REFLEX TIME TESTER";
  ssd1306_SetCursor(0, 20);
  ssd1306_WriteString(msg, Font_7x10, color);

  msg = "Press button to start";
  ssd1306_SetCursor(0, 45);
  ssd1306_WriteString(msg, Font_6x8, color);
  ssd1306_UpdateScreen();

  /* USER CODE END 2 */

  /* Initialize leds */
  BSP_LED_Init(LED2);

  /* Initialize USER push-button, will be used to trigger an interrupt each time it's pressed.*/
  BSP_PB_Init(BUTTON_USER, BUTTON_MODE_EXTI);

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */

  srand(HAL_GetTick());
  while (1)
  {
	if (mode == 0) {
		  char* msg2 = "Wait for 'NOW'...";
		  ssd1306_Fill(Black);
		  ssd1306_SetCursor(0, 5);
		  ssd1306_WriteString(msg2, Font_6x8, color);
		  ssd1306_UpdateScreen();

		  msg2 = "NOW";
		  ssd1306_Fill(Black);
		  ssd1306_SetCursor(40, 20);
		  ssd1306_WriteString(msg2, Font_16x24, color);
		  HAL_Delay(rand() % 3000 + 1000);
		  ssd1306_UpdateScreen();
		  mode = 2;
		  flag = 1;
		  __HAL_TIM_SET_COUNTER(&htim10, 0);
		  HAL_TIM_Base_Start(&htim10);
	}
	else if (mode == 1) {
		  timer_val = __HAL_TIM_GET_COUNTER(&htim10);
		  HAL_TIM_Base_Stop(&htim10);

		  float divider = 10000;
		  float timer_s = timer_val / divider;
		  total_time_s += timer_s;
		  avg_time_s = total_time_s / (counter + 1);

		  if (timer_s < best_time_s) {
			  best_time_s = timer_s;
		  }
		  char msg[64];
		  ssd1306_Fill(Black);

		  sprintf(msg, "Curr time: %.5f s", timer_s);
		  ssd1306_SetCursor(0, 0);
		  ssd1306_WriteString(msg, Font_6x8, color);

		  sprintf(msg, "Best time: %.5f s", best_time_s);
		  ssd1306_SetCursor(0, 15);
		  ssd1306_WriteString(msg, Font_6x8, color);

		  sprintf(msg, "Avg time: %.5f s", avg_time_s);
		  ssd1306_SetCursor(0, 30);
		  ssd1306_WriteString(msg, Font_6x8, color);

		  sprintf(msg, "Press button to retry");
		  ssd1306_SetCursor(0, 52);
		  ssd1306_WriteString(msg, Font_6x8, color);

		  ssd1306_UpdateScreen();

		  counter++;
		  mode = 2;
		  flag = 0;
	}
	else {

	}

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
